<template>
  <div class="home">
    <div>
        <h3>Bus Route List</h3>
        <pre>{{JSON.stringify(selectedRouteData)}}</pre>
        <p>
          <input @input="handleFilter" :value="filterText" />
        </p>
        <pre>{{filterText}}</pre>
      </div>
  </div>
</template>

<script lang="ts">
import Vue from "vue";
import { mapGetters, mapActions } from "vuex";

export default Vue.component("bus-routes", {
  computed: {
    ...mapGetters(["selectedRouteData", "filterText"])
  },
  methods: {
    ...mapActions(["filterRoutesByText"]),
    handleFilter(event: any) {
      const { value } = event.target;
      this.filterRoutesByText(value);
    }
  }
});
</script>


